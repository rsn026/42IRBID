void	u_d(int arr[4][4], char *str)
{
	int	i;

	i = 0;
	while (i < 4)
	{
		if (str[i] == '2' && str[i + 4] == '3')
			arr[1][i] = 4;
		else if (str[i] == '3' && str[i + 4] == '2' )
			arr[2][i] = 4;
		i++;
	}
}

void	l_r(int arr[4][4], char *str)
{
	int	i;
	int	r;

	i = 8;
	while (i < 12)
	{
		r = i - 8;
		if (str[i] == '2' && str[i + 4] == '3')
			arr[r][1] = 4;
		else if (str[i] == '3' && str [i + 4] == '2')
			arr[r][2] = 4;
		i++;
	}
}
